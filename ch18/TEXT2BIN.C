static unsigned char
*convert( unsigned char *Src,
			  unsigned char *Dest,
			  int Row,
			  int Count)
{
	int SourceChar;
	unsigned short Pattern;
	unsigned char *Save = Src;

	while (Count--)
	{
		SourceChar = *Src++;
		Pattern = CharacterSet [SourceChar & 0xff] [Row];
		*Dest++ = (unsigned char)((Pattern >> 8) & 0xff);
		*Dest++ = (unsigned char)(Pattern & 0xff);
	}

	return Save;
}

int
main(int argc, char **argv)
{
  /* FILE pointers for the text input and the     */
  /* converted binary output                      */
  FILE *input;
  FILE *output;

  /* a buffer to hold the text read from the file  */
  /* the size of the buffer guarantees that it can */
  /* receive CHARS_PER_ROW characters from fgets() */
  /* while having room for a '\n' and a '\0'       */
  char textbuff [CHARS_PER_ROW + 2];

  /* a buffer to hold the converted binary output  */
  /* one scan line at a time                       */
  unsigned char binary [OCTETS_PER_ROW];

  /* the character pointer below is used to check  */
  /* the text read by fgets() for a '\n', and to   */
  /* remove it if present                          */
  char *find_new_line;

  int row;    /* counts the scan line rows         */
  int count;  /* counts the lines converted        */

  /* check number of command line arguments and    */
  /* open the source and destination files, clean  */
  /* up and exit if an error occurs                */
  if (argc < 3)
  {
    fputs("usage: t2b text-file binary-file\n", stderr);
    return EXIT_FAILURE;
  }
  else if (NULL == (input = fopen(argv[1], "r")))
  {
    fprintf(stderr, "t2b: can't open %s", argv[1]);
    return EXIT_FAILURE;
  }
  else if (NULL == (output = fopen(argv[2], "wb")))
  {
    fprintf(stderr, "t2b: can't make %s", argv[2]);
    fclose(input);
    return EXIT_FAILURE;
  }

  /* display successful file open                  */
  fprintf(stderr, "t2b processing\n",
                  "reading text input %s\n",
                  "making binary output %s\n",
                  argv[1], argv[2]);

  /* 64 text lines converted at 16 scan rows per   */
  /* text line equals 1024 scan rows, which equals */
  /* approximately 269 millimeters                 */
  for (count = 0; count < LINES_PER_PAGE; ++count)
  {
    /* fill the input buffer with '\0' characters  */
    /* before each input in case the new line has  */
    /* fewer characters than the line before it    */
    memset(textbuff, '\0', sizeof(textbuff));

    if (NULL == fgets(textbuff, sizeof textbuff, input))
    {
      /* if the file has fewer than the maximum    */
      /* number of lines                           */
      fclose(input);
      fclose(output);
      break;
    }

    /* fgets() leaves the '\n' in the input buffer */
  	/* if it found one, so check for it and remove */
  	/* if present                                  */
    find_new_line = strchr(textbuff, '\n');
    if (find_new_line != NULL)
    {
      *find_new_line = '\0';
    }

    /* now convert to binary scan patterns         */
    for (row = 0; row < SCAN_ROWS_PER_CHAR; ++row)
    {
      convert((unsigned char *)textbuff,
               binary, row, CHARS_PER_ROW);

      /* write the binary to output file           */
      if ((fwrite(binary, 1, OCTETS_PER_ROW, output))
          != OCTETS_PER_ROW)
      {
        fprintf(stderr, "t2b: error writing %s\n", argv[2]);
        fclose(input);
        fclose(output);
        return EXIT_FAILURE;
      }
    }  /* end of ROWS_PER_CHAR repetitions         */
  }  /* end of TEXT_LINES_PER_PAGE or end of input */

  /* display the number of lines converted         */
  printf("Finished converting %d lines\n", count);
  return EXIT_SUCCESS;
}


